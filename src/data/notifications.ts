export function randomItem<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

export function buildHeroLine(): string {
  const name = randomItem(NAMES);
  const city = randomItem(CITIES);
  const action = randomItem(MATERIAL_ACTIONS);
  return `${name} из ${city} только что ${action}`;
}

export function buildCornerToast(): CornerToast {
  return {
    name: randomItem(NAMES),
    city: randomItem(CITIES),
    action: randomItem(MATERIAL_ACTIONS),
  };
}

export function getContextHint(context: string): string {
  const hints = CONTEXT_HINTS[context] ?? CONTEXT_HINTS.default;
  return randomItem(hints);
}

export function getDocActivityMessage(context: string): string {
  const msgs = DOC_ACTIVITY_MESSAGES[context] ?? DOC_ACTIVITY_MESSAGES.default;
  return randomItem(msgs);
}

export function getPopularityLabel(index: number): string | null {
  return LIST_POPULARITY_LABELS[index % LIST_POPULARITY_LABELS.length];
}

export function randomDownloadCount(): number {
  return 80 + Math.floor(Math.random() * 120);
}
