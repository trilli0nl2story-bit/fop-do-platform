export function getProductBySlug(slug: string): StoreProduct | undefined {
  return storeProducts.find(p => p.slug === slug);
}

export function getProductsByCategory(category: string): StoreProduct[] {
  if (category === 'Все категории') return storeProducts;
  return storeProducts.filter(p => p.category === category);
}

export interface BundleProduct {
  product: StoreProduct;
  monthName: string;
}

export interface BundleOffer {
  items: BundleProduct[];
  label: string;
}

export function getBundleOffer(product: StoreProduct): BundleOffer | null {
  if (!product.category.startsWith('КТП') || !product.ktpMonth || !product.ktpAgeKey) return null;

  const currentIdx = MONTH_ORDER.indexOf(product.ktpMonth as typeof MONTH_ORDER[number]);
  if (currentIdx === -1) return null;

  const candidates: BundleProduct[] = [];

  for (let delta = -2; delta <= 2; delta++) {
    if (delta === 0) continue;
    const idx = currentIdx + delta;
    if (idx < 0 || idx >= MONTH_ORDER.length) continue;
    const monthName = MONTH_ORDER[idx];
    const p = storeProducts.find(
      s => s.category === product.category &&
        s.ktpMonth === monthName &&
        s.ktpAgeKey === product.ktpAgeKey &&
        s.id !== product.id
    );
    if (p) candidates.push({ product: p, monthName });
  }

  if (candidates.length < 2) return null;

  const bundle = candidates.slice(0, 2);
  const monthNames = [product.ktpMonth, ...bundle.map(b => b.monthName)]
    .sort((a, b) => MONTH_ORDER.indexOf(a as typeof MONTH_ORDER[number]) - MONTH_ORDER.indexOf(b as typeof MONTH_ORDER[number]));

  return {
    items: bundle,
    label: monthNames.join(' + '),
  };
}

export type RelatedProductType = 'next_month' | 'previous_month' | 'same_age_related';

export interface RelatedProduct {
  type: RelatedProductType;
  product: StoreProduct;
}

const MONTH_ORDER = [
  'Сентябрь', 'Октябрь', 'Ноябрь', 'Декабрь',
  'Январь', 'Февраль', 'Март', 'Апрель', 'Май',
] as const;

function getAdjacentMonth(month: string, delta: 1 | -1): string | null {
  const idx = MONTH_ORDER.indexOf(month as typeof MONTH_ORDER[number]);
  if (idx === -1) return null;
  const next = idx + delta;
  if (next < 0 || next >= MONTH_ORDER.length) return null;
  return MONTH_ORDER[next];
}

export function getRelatedProducts(product: StoreProduct): RelatedProduct[] {
  const results: RelatedProduct[] = [];

  const isKtp = product.category.startsWith('КТП') && product.ktpMonth && product.ktpAgeKey;

  if (isKtp && product.ktpMonth && product.ktpAgeKey) {
    const nextMonthName = getAdjacentMonth(product.ktpMonth, 1);
    if (nextMonthName) {
      const nextMonth = storeProducts.find(
        p => p.category === product.category && p.ktpMonth === nextMonthName && p.ktpAgeKey === product.ktpAgeKey && p.id !== product.id
      );
      if (nextMonth) results.push({ type: 'next_month', product: nextMonth });
    }

    const prevMonthName = getAdjacentMonth(product.ktpMonth, -1);
    if (prevMonthName) {
      const prevMonth = storeProducts.find(
        p => p.category === product.category && p.ktpMonth === prevMonthName && p.ktpAgeKey === product.ktpAgeKey && p.id !== product.id
      );
      if (prevMonth) results.push({ type: 'previous_month', product: prevMonth });
    }

    const ageKey = product.ktpAgeKey!;
    const related =
      storeProducts.find(
        p => p.id !== product.id &&
          p.category === 'Диагностика' &&
          p.ageGroup === ageKey &&
          (p.program === product.program || p.program === 'ФОП ДО')
      ) ||
      storeProducts.find(
        p => p.id !== product.id &&
          p.category === 'Диагностика' &&
          p.ageGroup.includes(ageKey.split('–')[0]) &&
          (p.program === product.program || p.program === 'ФОП ДО')
      ) ||
      storeProducts.find(
        p => p.id !== product.id &&
          (p.category === 'Диагностика' || p.category === 'Рабочие программы') &&
          (p.program === product.program || p.program === 'ФОП ДО') &&
          p.id !== product.id
      );
    if (related) results.push({ type: 'same_age_related', product: related });
  } else {
    const sameCategory = storeProducts.filter(
      p => p.id !== product.id && p.category === product.category
    ).slice(0, 2);
    sameCategory.forEach(p => results.push({ type: 'same_age_related', product: p }));

    if (results.length < 3) {
      const sameAge = storeProducts.find(
        p => p.id !== product.id && p.ageGroup === product.ageGroup && !results.some(r => r.product.id === p.id)
      );
      if (sameAge) results.push({ type: 'same_age_related', product: sameAge });
    }
  }

  return results.slice(0, 3);
}
